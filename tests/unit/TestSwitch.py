    @attr(all=True, unit=True)
    def test_overstressed_mild_env(self):
        """
        (TestSwitch) _overstressed should return False and overstress=True on success with an overstressed condition in a non-harsh environment
        """

        self.DUT.environment_active = 1
        self.DUT.operating_current = 0.23
        self.DUT.rated_current = 0.25
        self.assertFalse(self.DUT._overstressed())
        self.assertTrue(self.DUT.overstress)

    @attr(all=True, unit=True)
    def test_overstressed_harsh_env(self):
        """
        (TestSwitch) _overstressed should return False and overstress=True on success with an overstressed condition in a harsh environment
        """

        self.DUT.environment_active = 3
        self.DUT.operating_current = 0.188
        self.DUT.rated_current = 0.25
        self.assertFalse(self.DUT._overstressed())
        self.assertTrue(self.DUT.overstress)
